clc;
clear all;
close all;
m = 8;
f = 20;
alpha = 0.2;
s = [0:0.1:1];
x = m.^2;
num = s.*alpha;
den = s.^2+alpha.^2;
z = (num./den);
T = 0.8 * x * z;
ns = 120 * f / 4;
nr = (1-s).*ns;
plot(ns,T,'b');
hold on;
f1 = 50;
ns1 = 120*f1/4;
nr1 = (1-s).*ns1;
plot(nr1,T,'r');
hold on;
xlabel('Speed');
ylabel('Torque');
title('Torque vs Speed in v/f control');
%frequency above 50 Hz
%For frequency 65 Hz, voltage = 415 V;
